
//
//  Check if number is less than or equal to zero
//  Very easy
//
func lessOrEqual(_ a: Double) -> Bool {
    return (a <= 0)
}

print(lessOrEqual(-1))
print(lessOrEqual(0))
print(lessOrEqual(1))

//
// In Swift:
//      Int = Number without a decimal point (2, -5, 0, 68, ...)
//      Double = Number with a decimal point (3.56, 8.01, 10.2, ...)
//
